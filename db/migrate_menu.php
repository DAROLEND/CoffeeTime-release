  price       DECIMAL(10,2) NOT NULL DEFAULT 0,
  pieces      TINYINT UNSIGNED NOT NULL DEFAULT 0,
  popularity  INT NOT NULL DEFAULT 0,
  INDEX (popularity)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4", "sushi_sets: таблиця створена");

