require_once __DIR__ . '/../db/db.php';

$log = [];
function run(mysqli $c, string $sql, string $label): void {
    global $log;
    if ($c->query($sql)) { $log[] = "✅ $label"; }
    else                  { $log[] = "❌ $label — " . $c->error; }
}

/* ══════════════════════════════════════════════
   1. ALTER pizza_items — add price_large
══════════════════════════════════════════════ */
$cols = [];
$r = $conn->query("SHOW COLUMNS FROM pizza_items");
while ($row = $r->fetch_assoc()) $cols[] = $row['Field'];
if (!in_array('price_large', $cols)) {
    run($conn, "ALTER TABLE pizza_items ADD COLUMN price_large DECIMAL(10,2) DEFAULT 0 AFTER price", "pizza_items: додано price_large");
}

/* ══════════════════════════════════════════════
   2. CREATE sushi_items
══════════════════════════════════════════════ */
run($conn, "CREATE TABLE IF NOT EXISTS sushi_items (
  id          INT AUTO_INCREMENT PRIMARY KEY,
  name        VARCHAR(255) NOT NULL,
  subcategory VARCHAR(80)  NOT NULL DEFAULT '',
  description TEXT,
  image       VARCHAR(255) NOT NULL DEFAULT 'static/images/menu_items/default.jpg',
  price       DECIMAL(10,2) NOT NULL DEFAULT 0,
  popularity  INT NOT NULL DEFAULT 0,
  INDEX (popularity)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4", "sushi_items: таблиця створена");

/* ══════════════════════════════════════════════
   3. CREATE sushi_sets
══════════════════════════════════════════════ */
